function qsort($array)
{
    $wickPoint = array_shift($array);
    $smallOne = $smallTwo = array();

    foreach ($array as $value) {
        counter();
        $value[0] > $wickPoint[0] ? array_push($smallOne, $value) : array_unshift($smallTwo, $value);
    }

    if (count(array_merge($smallTwo, $smallOne)) > 2) {
        return array_merge(qsort($smallTwo), array($wickPoint), qsort($smallOne));
    }

    return array_merge($smallTwo, array($wickPoint), $smallOne);
}

function insertion_sort($array)
{
    for ($i = 0; $i < count($array); $i++) {
        counter();
        for ($j = 0; $j < $i; $j++) {
            counter();
            if ($array[$i]{0} <= $array[$j]{0}) {
                $temp = $array[$j];
                $array[$j] = $array[$i];
                $array[$i] = $temp;
                break;
            }
        }
    }

    return $array;
}

function selection_sort(array $array)
{
    for ($i = 0; $i < count($array); $i++) {
        $min = false;
        for ($j = $i; $j < count($array); $j++) {
            counter();
            if ($array[$j][0] <= $min[0] || $min == false) {
                $min = $array[$j];
                $minKey = $j;
                continue;
            }
        }

        $array[$minKey] = $array[$i];
        $array[$i] = $min;
    }
    return $array;
}

function merge_sort(array $rawArray)
{
    if (count($rawArray) <= 1) {
        return $rawArray;
    }

    if (count($rawArray) > 2) {
        for ($i = 0; $i < count($rawArray); $i++) {
            $i < ceil(count($rawArray) / 2) ? $left[] = $rawArray[$i] : $right[] = $rawArray[$i];
        }

        $rawArray = array_merge(merge_sort($left), merge_sort($right));
    }

    $result = array();
    $l = 0;
    $r = ceil(count($rawArray) / 2);
    while (count($rawArray) != count($result)) {
        counter();
        if (($rawArray[$l][0] <= $rawArray[$r][0] && $l != ceil(count($rawArray) / 2) && $rawArray[$l] !== false) || $rawArray[$r] === false) {
            $result[] = $rawArray[$l];
            $l + 1 < ceil(count($rawArray) / 2) ? $l++ : $rawArray[$l] = false;
        } else {
            $result[] = $rawArray[$r];
            $r + 1 < count($rawArray) ? $r++ : $rawArray[$r] = false;
        }
    }

    return $result;
}


